using System;
using System.Collections.Generic;
using System.Text;

namespace Maticsoft.Common.model.httpmodel
{
    public class HttpResponseMsg<T>
    {
        private ResponseHead _head;//消息头
        private T _body;//消息体

        public ResponseHead head
        {
            get { return _head; }
            set { _head = value; }
        }
        public T body
        {
            get { return _body; }
            set { _body = value; }
        }
    }
}
